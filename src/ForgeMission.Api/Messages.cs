using System.Text.Json.Serialization;

namespace ForgeMission.Api;

// API A — mission invocation (42.6 task 5a). Message-based, not URL-shaped: see "API design —
// message-based" in docs/phases/phase-42.6-hosted-endpoint-ttfa.md for the full rationale and the
// locked invariants (M1-M10) these types implement. Naming follows the Amazon/ServiceStack
// convention — verb-named operation + XResponse — so a caller can infer the response type without
// consulting docs.

// ---------- Execute (the core operation) ----------

/// <summary>Run a mission once. M2: the mission handle is a field, never a route segment.</summary>
public sealed class ExecuteMission
{
    /// <summary>M3. PROTOCOL version — 0 means a pre-versioning client. Not the mission version.</summary>
    public int Version { get; set; }

    /// <summary>M7. Idempotency key, auto-generated by the CLI/SDK before send; a retry reuses it.</summary>
    public string ClientToken { get; set; } = "";

    /// <summary>"websearch" | "forge/websearch" — case-insensitive, Docker-style default namespace.
    /// See <see cref="MissionHandle"/>.</summary>
    public string Mission { get; set; } = "";

    /// <summary>Mission version/tag, distinct from <see cref="Version"/> (the protocol version).
    /// Null/absent means latest/currently-pinned.</summary>
    public string? MissionVersion { get; set; }

    /// <summary>Primary free-text input (maps to the runner's Goal).</summary>
    public string Input { get; set; } = "";

    /// <summary>Structured mission inputs (future — most missions read only <see cref="Input"/> today).</summary>
    public Dictionary<string, string>? Inputs { get; set; }

    /// <summary>Uploaded input artifact ids to stage for this run.</summary>
    public List<string>? InputArtifactIds { get; set; }

    /// <summary>M10. A message property, not an Accept header.</summary>
    public bool Stream { get; set; }
}

public sealed class ExecuteMissionResponse
{
    /// <summary>M6 — addressable independently of the connection that started the run.</summary>
    public string RunId { get; set; } = "";

    /// <summary>The RESOLVED handle — echoes what actually ran.</summary>
    public string Mission { get; set; } = "";

    /// <summary>Resolved version/OCI digest.</summary>
    public string MissionVersion { get; set; } = "";

    public string Answer { get; set; } = "";
    public bool Verified { get; set; }
    public List<MissionSource> Sources { get; set; } = [];
    public List<MissionTraceStep> Trace { get; set; } = [];
    public List<MissionArtifact> Artifacts { get; set; } = [];
    public MissionUsage Usage { get; set; } = new();

    /// <summary>M8 — usage/balance travel as response fields; an HTTP header is only a projection.</summary>
    public long BalanceMicroUsd { get; set; }

    /// <summary>M4.</summary>
    public ResponseStatus ResponseStatus { get; set; } = new();
}

public sealed class MissionUsage
{
    public long InputTokens { get; set; }
    public long OutputTokens { get; set; }
    public double ComputeSeconds { get; set; }
    public string? Model { get; set; }
    public long CostMicroUsd { get; set; }
}

/// <summary>Mirrors the engine's step envelope.</summary>
public sealed class MissionTraceStep
{
    public string Expert { get; set; } = "";
    public string Status { get; set; } = "";
    public string? Text { get; set; }
    public string? Reason { get; set; }
    public int Attempt { get; set; }
}

/// <summary>Mirrors Scout.SourceRef (src/ForgeMission.Scout/IWebSearch.cs) field-for-field — the
/// actual upstream type this is plumbed from once the runner contract carries structured citations
