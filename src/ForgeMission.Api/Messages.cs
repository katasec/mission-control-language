/// <summary>Transport-independent error codes — authoritative. The HTTP adapter projects these to
/// 404/402/401/400/403/500 as a convenience; the message is what's authoritative (M4). The spoke's
/// six-code list (MissionNotFound…RunFailed) didn't separately name a GetRun-not-found code, but its
/// own GetRunResponse doc comment says "e.g. RunNotFound" — an evident oversight, not a deliberate
/// choice to overload RunFailed (a 500) onto a 404 case. RunNotFound is added here as the same kind
/// of additive fix MissionNotFound already models — new values are added to the set, never invented
/// ad hoc at a call site (same rule <see cref="RunStatus"/> follows).</summary>
public static class ErrorCode
{
    public const string MissionNotFound = "MissionNotFound";
    public const string RunNotFound = "RunNotFound";
    public const string InsufficientCredit = "InsufficientCredit";
    public const string Unauthenticated = "Unauthenticated";
    public const string InvalidInput = "InvalidInput";
    public const string PolicyViolation = "PolicyViolation";
    public const string ArtifactNotFound = "ArtifactNotFound";
    public const string RunFailed = "RunFailed";
}

// ---------- Shared ----------

/// <summary>Matches ServiceStack.ResponseStatus/ResponseError verbatim (github.com/ServiceStack/
/// ServiceStack, src/ServiceStack.Interfaces/{ResponseStatus,ResponseError}.cs) — not a bespoke
/// shape. No TraceId field (correlation id lives in Meta, same as ServiceStack does it) and no
/// Warnings (was an orphan field with no defined use).</summary>
public sealed class ResponseStatus
{
    /// <summary>Null/empty == success.</summary>
    public string? ErrorCode { get; set; }
    public string? Message { get; set; }

    /// <summary>Only when server DebugMode is on; never in prod.</summary>
    public string? StackTrace { get; set; }

    /// <summary>Field-level validation errors, e.g. InvalidInput.</summary>
    public List<ResponseError>? Errors { get; set; }

    /// <summary>Additional context, e.g. Meta["traceId"].</summary>
    public Dictionary<string, string>? Meta { get; set; }

    public static ResponseStatus Ok() => new();

    public static ResponseStatus Fail(string errorCode, string message) =>
        new() { ErrorCode = errorCode, Message = message };
}

public sealed class ResponseError
{
    public string ErrorCode { get; set; } = "";
    public string FieldName { get; set; } = "";
    public string Message { get; set; } = "";
    public Dictionary<string, string>? Meta { get; set; }
}

// camelCase to match the framework's own default for the other four endpoints (minimal API's
// result-writer uses ASP.NET's ConfigureHttpJsonOptions default, camelCase) — ExecuteMission's
// manual JsonSerializer.SerializeToUtf8Bytes calls bypass that writer, so without this the wire
// was inconsistent: ExecuteMissionResponse came back PascalCase while GetAccount/SearchMissions/
// GetMission/GetRun came back camelCase. Caught while building the CLI client against the real
// deployed wire (2026-07-19) — case-insensitive on read regardless, but the emitted case must be
// one consistent thing for a hand-written client not to special-case per endpoint.
[JsonSourceGenerationOptions(PropertyNameCaseInsensitive = true, PropertyNamingPolicy = JsonKnownNamingPolicy.CamelCase)]
[JsonSerializable(typeof(ExecuteMission))]
[JsonSerializable(typeof(ExecuteMissionResponse))]
[JsonSerializable(typeof(MissionRunEvent))]
[JsonSerializable(typeof(UploadArtifact))]
[JsonSerializable(typeof(UploadArtifactResponse))]
[JsonSerializable(typeof(GetArtifact))]
[JsonSerializable(typeof(MissionArtifact))]
[JsonSerializable(typeof(SearchMissions))]
[JsonSerializable(typeof(SearchMissionsResponse))]
[JsonSerializable(typeof(GetMission))]
[JsonSerializable(typeof(GetMissionResponse))]
[JsonSerializable(typeof(GetAccount))]
[JsonSerializable(typeof(GetAccountResponse))]
[JsonSerializable(typeof(GetRun))]
[JsonSerializable(typeof(GetRunResponse))]
[JsonSerializable(typeof(ResponseStatus))]
internal partial class MessagesJsonContext : JsonSerializerContext;
